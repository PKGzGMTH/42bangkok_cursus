#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;

	i = 0;
	while (i < n)
		((char *)(s + i++))[0] = c;
	((char *)(s + i))[0] = 0;
	return (s);
}
