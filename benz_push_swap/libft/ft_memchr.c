#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*t;

	t = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		if (*t != (unsigned char)c)
			t++;
		else
			return (t);
		i++;
	}
	return (0);
}
